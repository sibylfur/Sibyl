const levelToCenter = (lvl) => {
  // low/mid/high mapped into MB coordinate centers within [-12, 0, +12]
  if (lvl === "low") return -12;
  if (lvl === "high") return 12;
  return 0;
};

function clamp(n, a, b){ return Math.max(a, Math.min(b, n)); }

function scoreToBand(n){
  // three bands with boundaries at -5 and +5 (user decided)
  if (n <= -5) return "low";
  if (n >= 5) return "high";
  return "mid";
}

function pickResult(totalM, totalB){
  // 1) compute band preference
  const mBand = scoreToBand(totalM);
  const bBand = scoreToBand(totalB);

  // 2) prefer results that match bands, but allow closest-distance fallback
  const candidates = DATA.results.map(r => {
    const mC = levelToCenter(r.center.M);
    const bC = levelToCenter(r.center.B);
    const dist = (totalM - mC) ** 2 + (totalB - bC) ** 2;

    const bandMatch = (r.center.M === mBand ? 1 : 0) + (r.center.B === bBand ? 1 : 0);
    // bandMatch higher is better; distance lower is better
    return { r, dist, bandMatch };
  });

  candidates.sort((a,b) => {
    if (b.bandMatch !== a.bandMatch) return b.bandMatch - a.bandMatch;
    return a.dist - b.dist;
  });

  return candidates[0].r;
}

function readState(){
  try{
    const raw = localStorage.getItem("maomao_state_v5");
    return raw ? JSON.parse(raw) : { step:0, M:0, B:0, picks:[] };
  }catch(e){
    return { step:0, M:0, B:0, picks:[] };
  }
}
function writeState(st){
  localStorage.setItem("maomao_state_v5", JSON.stringify(st));
}

function reset(){
  const st = { step:0, M:0, B:0, picks:[] };
  writeState(st);
  render();
}

function addPick(choice){
  const st = readState();
  st.M += choice.M || 0;
  st.B += choice.B || 0;
  st.picks.push({ text: choice.text, M: choice.M || 0, B: choice.B || 0 });
  st.step += 1;
  writeState(st);
  render();
}

